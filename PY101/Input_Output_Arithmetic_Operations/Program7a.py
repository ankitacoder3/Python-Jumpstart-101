'''
Problem Statement:
Program to  Convert temperature in Celsius to Fahrenheit
'''
print('\nCalculate Celsius_to_fahrenheit.py\n')

#Getting input for temperature in ‘Celsius’.
Celsius= float(input("Enter temperature in Celsius: "))

#Converting Celsius to Fahrenheit.
Fahrenheit = ( (9/5) * Celsius) + 32

#output
print("\nThe temperature is {} Fahrenheit.". format (Fahrenheit))
print()